// GenerateNPCSprites creates sprite sheets for NPCs.
func GenerateNPCSprites() map[string]*render.SpriteSheet {
	result := map[string]*render.SpriteSheet{}

	grids := map[string][16][16]byte{
		"merchant":   merchantGrid,
		"elder":      elderGrid,
		"blacksmith": blacksmithGrid,
		"innkeeper":  innkeeperGrid,
	}

	for name, grid := range grids {
		img := ebiten.NewImage(32, 16)
		drawGrid(img, grid, 0, 0)
		drawGrid(img, grid, 16, -1) // idle bob frame
		result[name] = render.NewSpriteSheet(img, 16, 16)
	}
	return result
}
