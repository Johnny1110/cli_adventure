// GenerateMonsterSprites creates sprite images for all monsters.
// Normal monsters are 16x16, boss (Dragon) is 32x32.
func GenerateMonsterSprites() map[string]*render.SpriteSheet {
	result := map[string]*render.SpriteSheet{}

	// 16x16 monsters: 2 frames each (idle bob)
	smallGrids := map[string][16][16]byte{
		"slime":    slimeGrid,
		"bat":      batGrid,
		"mushroom": mushroomGrid,
	}

	for name, grid := range smallGrids {
		img := ebiten.NewImage(32, 16)
		drawGrid(img, grid, 0, 0)
		drawGrid(img, grid, 16, -1) // bob frame
		result[name] = render.NewSpriteSheet(img, 16, 16)
	}

	// Dragon: 32x32, 2 frames
	dragonImg := ebiten.NewImage(64, 32)
	drawGrid32(dragonImg, dragonGrid, 0, 0)
	drawGrid32(dragonImg, dragonGrid, 32, -1)
	result["dragon"] = render.NewSpriteSheet(dragonImg, 32, 32)

	return result
}

// drawGrid32 renders a 32x32 grid onto an image at the given offset.
func drawGrid32(dst *ebiten.Image, grid [32][32]byte, offsetX, offsetY int) {
	for y := 0; y < 32; y++ {
		for x := 0; x < 32; x++ {
			idx := grid[y][x]
			if idx == 0 || int(idx) >= len(spritePalette) {
				continue
			}
			px := offsetX + x
			py := offsetY + y
			if px >= 0 && py >= 0 {
				dst.Set(px, py, spritePalette[idx])
			}
		}
	}
}
