// fontImage is the pre-rendered font atlas, built once at init.
var fontImage *ebiten.Image

// glyphPositions maps each rune to its (x, y) position in the font atlas.
var glyphPositions = map[rune][2]int{}

func init() {
	buildFontImage()
}

// buildFontImage renders all glyph bitmaps into a single *ebiten.Image atlas.
func buildFontImage() {
	cols := 16
	rows := 8
	fontImage = ebiten.NewImage(cols*GlyphW, rows*GlyphH)

	charIndex := 0
	for ch, data := range glyphData {
		cx := (charIndex % cols) * GlyphW
		cy := (charIndex / cols) * GlyphH

		for y, row := range data {
			for x, pixel := range row {
				if pixel == '#' {
					fontImage.Set(cx+x, cy+y, color.White)
				}
			}
		}
		glyphPositions[ch] = [2]int{cx, cy}
		charIndex++
	}
}

// DrawText renders a string at (x, y) with the given color onto dst.
func DrawText(dst *ebiten.Image, text string, x, y int, clr color.Color) {
	curX := x
	curY := y
	for _, ch := range text {
		if ch == '\n' {
			curX = x
			curY += GlyphH + 2
			continue
		}

		pos, ok := glyphPositions[ch]
		if !ok {
			curX += GlyphW + GlyphGap
			continue
		}

		sx, sy := pos[0], pos[1]
		glyphRect := image.Rect(sx, sy, sx+GlyphW, sy+GlyphH)

		op := &ebiten.DrawImageOptions{}
		op.GeoM.Translate(float64(curX), float64(curY))

		// Tint the white glyph to the desired color
		r, g, b, a := clr.RGBA()
		op.ColorScale.ScaleWithColor(color.RGBA{
			R: uint8(r >> 8),
			G: uint8(g >> 8),
			B: uint8(b >> 8),
			A: uint8(a >> 8),
		})

		dst.DrawImage(fontImage.SubImage(glyphRect).(*ebiten.Image), op)
		curX += GlyphW + GlyphGap
	}
}

// TextWidth returns the pixel width of a rendered string.
func TextWidth(text string) int {
	w := 0
	for _, ch := range text {
		if ch == '\n' {
			continue
		}
		w += GlyphW + GlyphGap
	}
	if w > 0 {
		w -= GlyphGap // no trailing gap
	}
	return w
}
