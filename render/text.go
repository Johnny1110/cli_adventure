// Package render provides drawing helpers for sprites, text, and UI elements.
//
// THEORY — Bitmap fonts:
// A bitmap font is a sprite sheet where each character is a fixed-size cell.
// To render text, we walk the string character by character, look up each glyph's
// position in the font atlas, and draw its sub-image. This gives pixel-perfect
// retro text with no anti-aliasing — crisp pixel glyphs that match the art style.
//
// The font atlas is a single *ebiten.Image containing all glyphs. Drawing text
// is just a series of SubImage blits, which Ebitengine batches efficiently on the GPU.
